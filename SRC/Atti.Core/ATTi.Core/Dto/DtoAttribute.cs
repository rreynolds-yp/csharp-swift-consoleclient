using System;

namespace ATTi.Core.Dto
{
	/// <summary>
	/// Marks an interface as a data transfer object.
	/// </summary>
	/// <remarks>
	/// Data tranfer objects are emitted and bound at runtime. All interfaces
	/// within the same assembly intended to be used as DTOs should be marked
	/// so the framework can emit all DTOs into a single assembly.
	/// </remarks>
	[AttributeUsage(AttributeTargets.Interface)]
	public class DtoAttribute : Attribute { }

}
